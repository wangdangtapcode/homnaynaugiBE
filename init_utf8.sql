        FOREIGN KEY(recipe_id)
        REFERENCES recipes(id)
        ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: view_history
CREATE TABLE view_history (
    id INT AUTO_INCREMENT PRIMARY KEY,
