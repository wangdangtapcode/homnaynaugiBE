-- MySQL Script with English Names
-- Table: roles
CREATE TABLE roles (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) UNIQUE NOT NULL COMMENT 'Role name, e.g., admin, user'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: accounts
CREATE TABLE accounts (
    id CHAR(36) PRIMARY KEY COMMENT 'UUID của tài khoản, được tạo bởi ứng dụng',
    username VARCHAR(255) UNIQUE NOT NULL COMMENT 'Tên đăng nhập duy nhất (có thể là email hoặc username riêng)',
    password_hash VARCHAR(255) NOT NULL COMMENT 'Mật khẩu đã được mã hóa',
    status ENUM('active', 'inactive', 'pending_verification', 'banned') NOT NULL DEFAULT 'pending_verification' COMMENT 'Trạng thái của tài khoản',
    last_login_at TIMESTAMP NULL COMMENT 'Thời điểm đăng nhập cuối cùng',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
-- Table: users
-- Table: user_profiles
CREATE TABLE user_profiles (
    id INT AUTO_INCREMENT PRIMARY KEY COMMENT 'ID tự tăng của hồ sơ người dùng',
    account_id CHAR(36) UNIQUE NOT NULL COMMENT 'UUID của tài khoản liên kết (khóa ngoại)',
    email VARCHAR(255) UNIQUE NOT NULL COMMENT 'Địa chỉ email duy nhất (dùng để liên lạc, thông báo)',
    phone_number VARCHAR(50) NULL,
    display_name VARCHAR(255) NULL COMMENT 'Tên hiển thị của người dùng',
    avatar_url TEXT NULL COMMENT 'URL hoặc đường dẫn đến ảnh đại diện',
    full_name VARCHAR(255) NULL COMMENT 'Họ và tên đầy đủ',
    address TEXT NULL COMMENT 'Địa chỉ',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT fk_user_profiles_account_id
        FOREIGN KEY (account_id)
        REFERENCES accounts(id)
        ON DELETE CASCADE -- Nếu tài khoản bị xóa, hồ sơ người dùng liên quan cũng bị xóa
        ON UPDATE CASCADE -- Nếu ID tài khoản thay đổi, ID trong hồ sơ cũng thay đổi
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
-- Table: account_roles
CREATE TABLE account_roles (
    id INT AUTO_INCREMENT PRIMARY KEY,
    account_id CHAR(36) NOT NULL COMMENT 'UUID của tài khoản (khóa ngoại)',
    role_id INT NOT NULL COMMENT 'ID của vai trò (khóa ngoại)',
    assigned_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT 'Thời điểm vai trò được gán',
    is_active BOOLEAN DEFAULT TRUE COMMENT 'Vai trò này có đang hoạt động cho tài khoản này không',
    -- Bạn có thể thêm các thông tin khác nếu cần, ví dụ: ngày hết hạn của vai trò
    -- expires_at TIMESTAMP NULL,
    CONSTRAINT fk_account_roles_account_id
        FOREIGN KEY (account_id)
        REFERENCES accounts(id)
        ON DELETE CASCADE
        ON UPDATE CASCADE,
    CONSTRAINT fk_account_roles_role_id
        FOREIGN KEY (role_id)
        REFERENCES roles(id)
        ON DELETE CASCADE -- Nếu vai trò bị xóa, các gán vai trò đó cũng bị xóa
        ON UPDATE CASCADE,
    UNIQUE KEY uk_account_role (account_id, role_id) 
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
-- Table: recipe_categories
CREATE TABLE recipe_categories (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) UNIQUE NOT NULL,
    image_url TEXT NULL COMMENT 'URL or path to the category image'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: ingredient_categories (Giữ nguyên)
CREATE TABLE ingredient_categories (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) UNIQUE NOT NULL,
    image_url TEXT NULL COMMENT 'URL or path to the category image'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: ingredients 
CREATE TABLE ingredients (
    id CHAR(36) PRIMARY KEY COMMENT 'Ingredient UUID, generated by the application',
    name VARCHAR(255) NOT NULL,
    image_url TEXT NULL COMMENT 'URL or path to the ingredient image'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: ingredient_category_mappings (BẢNG TRUNG GIAN MỚI)
CREATE TABLE ingredient_category_mappings (
    ingredient_id CHAR(36) NOT NULL,
    ingredient_category_id INT NOT NULL,
    PRIMARY KEY (ingredient_id, ingredient_category_id), -- Khóa chính kết hợp
    CONSTRAINT fk_ing_cat_map_ingredient_id
        FOREIGN KEY(ingredient_id)
        REFERENCES ingredients(id)
        ON DELETE CASCADE, -- Nếu nguyên liệu bị xóa, mapping cũng bị xóa
    CONSTRAINT fk_ing_cat_map_category_id
        FOREIGN KEY(ingredient_category_id)
        REFERENCES ingredient_categories(id)
        ON DELETE CASCADE -- Nếu danh mục bị xóa, mapping cũng bị xóa
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: recipes
CREATE TABLE recipes (
    id CHAR(36) PRIMARY KEY COMMENT 'UUID của công thức, được tạo bởi ứng dụng',
    -- Đổi user_id thành account_id và tham chiếu đến bảng accounts
    account_id CHAR(36) NULL COMMENT 'UUID của tài khoản người tạo công thức',
    name VARCHAR(255) NOT NULL,
    description TEXT NULL,
    protein DECIMAL(10,2) NULL,
    fat DECIMAL(10,2) NULL,
    calories INT NULL,
    carbohydrates DECIMAL(10,2) NULL,
    image_url TEXT NULL COMMENT 'URL hoặc đường dẫn đến ảnh công thức',
    preparation_time_minutes INT NULL COMMENT 'Thời gian hoàn thành công thức (ví dụ: tính bằng phút)',
    video_url TEXT NULL COMMENT 'URL video hướng dẫn',
    status ENUM('published', 'pending_approval', 'rejected', 'draft', 'public') NOT NULL DEFAULT 'draft' COMMENT 'Trạng thái của công thức',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    CONSTRAINT fk_recipes_account_id -- Đổi tên ràng buộc
        FOREIGN KEY(account_id)     -- Đổi cột tham chiếu
        REFERENCES accounts(id)     -- Tham chiếu đến bảng accounts
        ON DELETE SET NULL -- Nếu tài khoản bị xóa, công thức vẫn còn nhưng không rõ người tạo
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


-- Table: recipe_category_mappings (BẢNG TRUNG GIAN MỚI)
CREATE TABLE recipe_category_mappings (
    recipe_id CHAR(36) NOT NULL,
    recipe_category_id INT NOT NULL,
    PRIMARY KEY (recipe_id, recipe_category_id), -- Khóa chính kết hợp
    CONSTRAINT fk_recipe_cat_map_recipe_id
        FOREIGN KEY(recipe_id)
        REFERENCES recipes(id)
        ON DELETE CASCADE, -- Nếu công thức bị xóa, mapping cũng bị xóa
    CONSTRAINT fk_recipe_cat_map_category_id
        FOREIGN KEY(recipe_category_id)
        REFERENCES recipe_categories(id)
        ON DELETE CASCADE -- Nếu danh mục bị xóa, mapping cũng bị xóa
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: units_of_measure
CREATE TABLE units_of_measure (
    id INT AUTO_INCREMENT PRIMARY KEY,
    unit_name VARCHAR(100) UNIQUE NOT NULL COMMENT 'e.g., Gram, Milliliter, Piece, Tablespoon',
    symbol VARCHAR(20) UNIQUE NULL COMMENT 'e.g., g, ml, pcs, tbsp'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: recipe_ingredients
CREATE TABLE recipe_ingredients (
    recipe_id CHAR(36) NOT NULL,
    ingredient_id CHAR(36) NOT NULL,
    quantity DECIMAL(10,2) NULL,
    unit_id INT NULL,
    PRIMARY KEY (recipe_id, ingredient_id),
    CONSTRAINT fk_recipe_ingredients_recipe_id
        FOREIGN KEY(recipe_id)
        REFERENCES recipes(id)
        ON DELETE CASCADE,
    CONSTRAINT fk_recipe_ingredients_ingredient_id
        FOREIGN KEY(ingredient_id)
        REFERENCES ingredients(id)
        ON DELETE CASCADE,
    CONSTRAINT fk_recipe_ingredients_unit_id
        FOREIGN KEY(unit_id)
        REFERENCES units_of_measure(id)
        ON DELETE RESTRICT -- Or SET NULL if an undefined unit is allowed
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: cooking_steps
CREATE TABLE cooking_steps (
    id INT AUTO_INCREMENT PRIMARY KEY,
    recipe_id CHAR(36) NOT NULL,
    step_order INT NOT NULL COMMENT 'Order of the cooking step',
    instruction TEXT NOT NULL,
    image_url TEXT NULL COMMENT 'Illustrative image for the step, can be NULL',
    CONSTRAINT fk_cooking_steps_recipe_id
        FOREIGN KEY(recipe_id)
        REFERENCES recipes(id)
        ON DELETE CASCADE,
    UNIQUE KEY uk_cooking_steps_recipe_order (recipe_id, step_order)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: recipe_likes
CREATE TABLE recipe_likes (
    -- Đổi user_id thành account_id và tham chiếu đến bảng accounts
    account_id CHAR(36) NOT NULL COMMENT 'UUID của tài khoản đã thích',
    recipe_id CHAR(36) NOT NULL,
    liked_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (account_id, recipe_id), -- Khóa chính kết hợp
