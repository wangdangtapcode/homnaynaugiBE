        FOREIGN KEY(account_id)           -- Đổi cột tham chiếu
        REFERENCES accounts(id)           -- Tham chiếu đến bảng accounts
        ON DELETE CASCADE, -- Nếu tài khoản bị xóa, các lượt thích của họ cũng bị xóa
    CONSTRAINT fk_recipe_likes_recipe_id
        FOREIGN KEY(recipe_id)
        REFERENCES recipes(id)
        ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Table: favorite_recipes
